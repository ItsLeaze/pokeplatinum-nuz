VisibleItems_TryGiveItem:
    PlaySE SEQ_SE_CONFIRM
    LockAll
    FacePlayer
    WaitSE SEQ_SE_CONFIRM
    SetVar VAR_0x8004, VAR_0x8008
    SetVar VAR_0x8005, VAR_0x8009
    CanFitItem VAR_0x8004, VAR_0x8005, VAR_RESULT
    GoToIfEq VAR_RESULT, TRUE, VisibleItems_GiveItem
    GoToIfEq VAR_RESULT, FALSE, VisibleItems_BagIsFull
    End

VisibleItems_End:
    CloseMessage
    ReleaseAll
    End

VisibleItems_GiveItem:
    Common_CheckBagPocketForItem
    RemoveObject VAR_LAST_TALKED
    AddItem VAR_0x8004, VAR_0x8005, VAR_RESULT
    TrySetUnusedCollectedOrbFlag VAR_0x8004
    CallIfEq VAR_0x8004, ITEM_STORAGE_KEY, VisibleItems_SetAltMusicGalacticHQ1F
    IsItemTMHM VAR_0x8004, VAR_RESULT
    GoToIfEq VAR_RESULT, TRUE, VisibleItems_MessagePlayerFoundTM
    GoToIfEq VAR_RESULT, FALSE, VisibleItems_MessagePlayerFoundItem
    End

VisibleItems_CheckPlateOrItemPocket:
    WaitFanfare
    CheckItemIsPlate VAR_0x8004, VAR_RESULT
    CallIfEq VAR_RESULT, TRUE, VisibleItems_MessageObtainedPlate
    BufferPlayerName 0
    BufferItemName 1, VAR_0x8004
    GetItemPocket VAR_0x8004, VAR_RESULT
    SetVar VAR_0x8008, VAR_RESULT
    GoToIfEq VAR_0x8008, POCKET_KEY_ITEMS, VisibleItems_BufferPocketNameKeyItems
    GoToIfEq VAR_0x8008, POCKET_ITEMS, VisibleItems_BufferPocketNameItems
    GoToIfEq VAR_0x8008, POCKET_BERRIES, VisibleItems_BufferPocketNameBerries
    GoToIfEq VAR_0x8008, POCKET_MEDICINE, VisibleItems_BufferPocketNameMedicine
    GoToIfEq VAR_0x8008, POCKET_BALLS, VisibleItems_BufferPocketNameBalls
    GoToIfEq VAR_0x8008, POCKET_BATTLE_ITEMS, VisibleItems_BufferPocketNameBattleItems
    GoToIfEq VAR_0x8008, POCKET_MAIL, VisibleItems_BufferPocketNameMail
    GoToIfEq VAR_0x8008, POCKET_TMHMS, VisibleItems_BufferPocketNameTMHMs
    End

